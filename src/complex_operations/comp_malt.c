#include "../../headers/fract_ol.h"

void	comp_malt(t_comp *a, t_comp *b, t_comp *c)
{
	c->r = a->r * b->r - a->i * b->i;
	c->i = a->r * b->i + a->i * b->r;
	return ;
}
